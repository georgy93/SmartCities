

-- Fill dbo.Customer with Values
DECLARE @counter INT = 1;
DECLARE @Max INT = 31
WHILE @counter <= @Max 
BEGIN 

	DECLARE @LocalNumber VARCHAR(20) = @counter
	DECLARE @LocalGender VARCHAR(1)
	DECLARE @LocalAge INT 
	
	--- region Number ---
	IF (@counter >= 1000000)
		BEGIN
			SET @LocalNumber = CAST(@LocalNumber AS VARCHAR(20))
		END
	ELSE IF (@counter >= 100000)
		BEGIN
			SET @LocalNumber = '0' + CAST(@LocalNumber AS VARCHAR(20))
		END
	ELSE IF (@counter >= 10000)
		BEGIN
			SET @LocalNumber = '00' + CAST(@LocalNumber AS VARCHAR(20))
		END
	ELSE IF (@counter >= 1000)
		BEGIN
			SET @LocalNumber = '000' + CAST(@LocalNumber AS VARCHAR(20))
		END
	ELSE IF (@counter >= 100)
		BEGIN
			SET @LocalNumber = '0000' + CAST(@LocalNumber AS VARCHAR(20))
		END
	ELSE IF (@counter >= 10)
		BEGIN
			SET @LocalNumber = '00000' + CAST(@LocalNumber AS VARCHAR(20))
		END
	ELSE 
		BEGIN 
			SET @LocalNumber = '000000' + CAST(@LocalNumber AS VARCHAR(20))
		END
	--- End region Number ---

	--- Region Gender ---
	-- Get a random number from 1 to 10
	-- if it is more than 5 then gender is M
	-- if it is more than 1 then gender is F
	-- if it is less than 2 it is unknown (NULL)
	DECLARE @GenderNumber INT
	SET @GenderNumber =  CEILING((10)*RAND())

	IF (@GenderNumber >= 6)
		BEGIN
			SET @LocalGender = 'M'
		END
	ELSE IF (@GenderNumber >= 2)
		BEGIN
			SET @LocalGender = 'F'
		END
	ELSE
		BEGIN
			SET @LocalGender = '?'	
		END
	--- End Region Gender ---
	
	SET @LocalNumber = '+42195' + @LocalNumber
	SET @LocalAge = CEILING(17 + (100-17)*RAND())

    INSERT INTO dbo.Customer
	(Number, Gender, Age) 
    VALUES(@LocalNumber, @LocalGender, @LocalAge); 

    SET @counter = @counter + 1;
END


